/**
 * 查词：返回匹配的词典条目，或 null
 * @param {string} word
 * @returns {{ phonetic: string, mean: string, sentence: string } | null}
 */
export function lookupWord(word) {
  if (!word) return null
  const key = word.trim().toLowerCase()
  return DICT[key] || null
}

/**
 * 搜索词典（模糊匹配）
 * @param {string} query
 * @returns {Array<{ word: string, phonetic: string, mean: string }>}
 */
export function searchDict(query) {
  if (!query) return []
  const q = query.toLowerCase().trim()
  return Object.entries(DICT)
    .filter(([word]) => word.startsWith(q))
    .slice(0, 10)
    .map(([word, data]) => ({ word, ...data }))
}

/**
 * 获取建议数量
 */
export function dictSize() {
  return Object.keys(DICT).length
}
